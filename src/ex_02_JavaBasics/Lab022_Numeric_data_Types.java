package ex_02_JavaBasics;

public class Lab022_Numeric_data_Types {
    public static void main(String[] args) {
        // Integer Data type
        byte a = 127; //  1 byte(8 bits) -128 to 127
        short b = 128;//  2 byte(16 bits)
        int c = 100; //   4 byte(32 bits)
        long d = 122323222L; // 8 byte (64 bits)

        // Floating data type
        float e =12.45f;
        double f = 12.867867668;

        System.out.println("The byte Value is :"+a);
        System.out.println("The short Value is :"+b);
        System.out.println("The int Value is :"+c);
        System.out.println("The long Value is :"+d);
        System.out.println("The float Value is :"+e);
        System.out.println("The double Value is :"+f);


    }
}
