package ex_04_operators;

public class Lab042_Interview_Concat_Plus {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a + b); // Math (Int perform addition of 2 numbers)

        String name1 = "Vaibhav";
        String name2 = "Sankpal";
        System.out.println(name1+name2); // Concatenation (String perform Conactentaion)


        // + -> behave differently with the data type.
        // + -> operator overloading
    }
}