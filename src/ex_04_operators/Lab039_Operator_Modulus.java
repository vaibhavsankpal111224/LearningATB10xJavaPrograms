package ex_04_operators;

public class Lab039_Operator_Modulus {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a%b); // Output is remainder ->0
        // Mod -> Remainder,
//        10 | 20 |  2 - Quoinet
//           | 20 |
//           -------
//             0 - Remainder
//           ----
        System.out.println(13%7); // Output is remainder ->6
//        07 | 13 |  1 - Quoinet
//           | 07 |
//           -------
//             6 - Remainder
    }
}