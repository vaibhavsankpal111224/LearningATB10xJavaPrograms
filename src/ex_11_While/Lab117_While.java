package ex_11_While;

public class Lab117_While {
    public static void main(String[] args) {
        int i = 0; // I
        while (i < 10) { // C
            System.out.println(i);
            i++; // U
        }

        //  I, C, U
        for (int j = 0; j < 10 ; j++) {
            System.out.println(j);
        }


    }
}
